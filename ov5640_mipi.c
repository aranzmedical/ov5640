/*!
 * ioctl_s_ctrl - V4L2 sensor interface handler for VIDIOC_S_CTRL ioctl
 * @s: pointer to standard V4L2 device structure
 * @vc: standard V4L2 VIDIOC_S_CTRL ioctl structure
 *
 * If the requested control is supported, sets the control's current
 * value in HW (and updates the video_control[] array).  Otherwise,
 * returns -EINVAL if the control is not supported.
 */
static int ioctl_s_ctrl(struct v4l2_int_device *s, struct v4l2_control *vc)
{
	int retval = 0;
	u8 reg_value = 0;
	unsigned char status,i;
	pr_debug("In ov5640:ioctl_s_ctrl %d\n",
		 vc->id);

	if (vc->id & V4L2_CID_REGISTERS_BASE) {
		uint16_t addr = vc->id & 0x0000FFFF;
		if (addr == 0)
			return -EINVAL;

		uint8_t value = vc->value;
		ov5640_write_reg(addr, value);
		return 0;
	}

	switch (vc->id) {
	case V4L2_CID_BRIGHTNESS:
	{
		if ((vc->value >= -64) && (vc->value <= 64)) {
			WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
			WRITE_REG_WITH_OVT_MAST(0x5580, 0x04, 0x04);

			if(vc->value >= 0) {
				ov5640_write_reg(0x5587, 0xFF & (vc->value));
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x08);
			}else {
				ov5640_write_reg(0x5587, 0xFF & (-vc->value));
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x08, 0x08);
			}
			ov5640_data.brightness = vc->value;
		}
		return 0;
	}break;
	case V4L2_CID_CONTRAST:
	{
		if ((vc->value >= -12) && (vc->value <= 12)) {
			WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
			WRITE_REG_WITH_OVT_MAST(0x5580, 0x04, 0x04);
			ov5640_write_reg(0x5585, 0x20 + (vc->value));
			ov5640_write_reg(0x5586, 0x20 + (vc->value));
			ov5640_data.contrast = vc->value;
		}
		return 0;
	}break;
	case V4L2_CID_SATURATION:
	{
		if ((vc->value >= -48) && (vc->value <= 48)) {
			WRITE_REG_WITH_OVT_MAST(0x5580, 0x02, 0x1A);
			WRITE_REG_WITH_OVT_MAST(0x5588, 0x40, 0x40);
			WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
	
			ov5640_write_reg(0x5583, 0x30 + (vc->value));
			ov5640_write_reg(0x5584, 0x30 + (vc->value));
			ov5640_data.saturation = vc->value;
		}
		return 0;
	}break;
	case V4L2_CID_HUE:
	{
		vc->value -= (vc->value % 30);
		if((vc->value >= -180) && (vc->value <= 150))
		{
			WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
			WRITE_REG_WITH_OVT_MAST(0x5580, 0x01, 0x01);
	
			switch(vc->value)
			{
				case -180:
				{
					ov5640_write_reg(0x5581, 0x80);
					ov5640_write_reg(0x5582, 0x00);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x32, 0x33);
				}break;
	
				case -150:
				{
					ov5640_write_reg(0x5581, 0x6F);
					ov5640_write_reg(0x5582, 0x40);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x32, 0x33);
				}break;
	
				case -120:
				{
					ov5640_write_reg(0x5581, 0x40);
					ov5640_write_reg(0x5582, 0x6F);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x32, 0x33);
				}break;
	
				case -90:
				{
					ov5640_write_reg(0x5581, 0x0);
					ov5640_write_reg(0x5582, 0x80);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x02, 0x33);
				}break;

				case -60:
				{
					ov5640_write_reg(0x5581, 0x40);
					ov5640_write_reg(0x5582, 0x6F);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x02, 0x33);
				}break;
	
				case -30:
				{
					ov5640_write_reg(0x5581, 0x6F);
					ov5640_write_reg(0x5582, 0x40);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x02, 0x33);
				}break;

				case 0:
				{
					ov5640_write_reg(0x5581, 0x80);
					ov5640_write_reg(0x5582, 0x00);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x01, 0x33);
				}break;

				case 30:
				{
					ov5640_write_reg(0x5581, 0x6f);
					ov5640_write_reg(0x5582, 0x40);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x01, 0x33);
				}break;

				case 60:
				{
					ov5640_write_reg(0x5581, 0x40);
					ov5640_write_reg(0x5582, 0x6f);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x01, 0x33);
				}break;
	
				case 90:
				{
					ov5640_write_reg(0x5581, 0x00);
					ov5640_write_reg(0x5582, 0x80);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x31, 0x33);
				}break;
	
				case 120:
				{
					ov5640_write_reg(0x5581, 0x40);
					ov5640_write_reg(0x5582, 0x6f);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x31, 0x33);
				}break;
	
				case 150:
				{
					ov5640_write_reg(0x5581, 0x6f);
					ov5640_write_reg(0x5582, 0x40);
					WRITE_REG_WITH_OVT_MAST(0x5588, 0x31, 0x33);
				}break;
			}
			ov5640_data.hue = vc->value;
		}
		return 0;
	}break;
	case V4L2_CID_AUTO_WHITE_BALANCE:
	{
		if (vc->value)
			vc->value = 1;
		else
			vc->value = 0;

		ov5640_data.aw_mode = vc->value;
		return planckian_locus_lookuptable(vc->value);
	}break;
	case V4L2_CID_WHITE_BALANCE_TEMPERATURE: {
		if ((vc->value >=3000) && (vc->value <=6000)) {
			ov5640_data.wb_temp = vc->value;
			vc->value -= vc->value%1000;
			return planckian_locus_lookuptable(vc->value);
		}
	}break;
	case V4L2_CID_HFLIP: {
		if ((vc->value >= 0) && (vc->value <= 1)) {
			ov5640_read_reg(0x3821, &reg_value);
			if (vc->value) {
				reg_value &= 0x01;
			}else {
				reg_value |= 0x07;
			}
			ov5640_write_reg(0x3821, reg_value);
			ov5640_data.hflip = vc->value;
		}
		return 0;
	}break;
	case V4L2_CID_VFLIP: {
		if ((vc->value >= 0) && (vc->value <= 1) ) {
			ov5640_read_reg(0x3820, &reg_value);
	
			if (vc->value) {
				reg_value |= 0x06;
			}else {
				reg_value &= ~(0x06);
			}
			ov5640_write_reg(0x3820, reg_value);
			ov5640_data.vflip = vc->value;
		}
		return 0;
	}break;
	
	case V4L2_CID_FOCUS_TRIGGER: {
		//unsigned char status,i;

		ov5640_write_reg(0x3023, 0x01);     
		ov5640_write_reg(0x3022, 0x03);	    

		for(i=0; i<5; i++) {
			ov5640_read_reg(0x3023, &status);
		
			if(status == 0x00)
				break;
			msleep(10);
		}	

		ov5640_write_reg(0x3023, 0x01);
		ov5640_write_reg(0x3022, 0x12);

		mdelay(10);

		for (i=0; i<5; i++) {
			ov5640_read_reg(0x3023, &status);
			
			if (status ==0x00)
				break;
			mdelay(10);
		}
		return 0;
	}break;
	
	case V4L2_CID_AUTO_FOCUS_START: /* Continuous AF Start*/
		//unsigned char i,status;
		printk("V4L2_CID_AUTO_FOCUS_START \n");

		ov5640_write_reg(0x3023, 0x01);        //Enable Continuous Focus
		ov5640_write_reg(0x3022, 0x04);	    //Enable Continuous Focus
		for(i=0 ;i<5;i++)
		{
			ov5640_read_reg(0x3023, &status);//Get Af Status
			if(status == 0x00)
			{
				break;
			}
			msleep(10);
		}
		ov5640_data.focus_step = 0;
	
		break;
	case V4L2_CID_AUTO_FOCUS_STOP: /* Continuous AF Stop*/
		//unsigned char i,status;
		printk("V4L2_CID_AUTO_FOCUS_STOP \n");

		ov5640_write_reg(0x3023, 0x01);     //Release Continuous Focus
		ov5640_write_reg(0x3022, 0x06);	    //Release Continuous Focus
		mdelay(10);
		for(i=0 ;i<5;i++)
		{
			ov5640_read_reg(0x3023, &status);//Get Af Status
			if(status == 0x0)
			{
				break;
			}
			mdelay(10);
		}		
		break;
	
	case V4L2_CID_FOCUS_ABSOLUTE: 
		/* Enter manual focus stub */
		if ((vc->value >= 0) && (vc->value <= 255)) {
			/* Disable Auto Focus */
			//unsigned char i,status;
			ov5640_write_reg(0x3027, 0x00);
			ov5640_write_reg(0x3028, vc->value);
			ov5640_write_reg(0x3023, 0x01);
			ov5640_write_reg(0x3022, 0x1a);
					
			for(i=0 ;i<5;i++)
			{
				ov5640_read_reg(0x3023, &status);//Get Af Status
				
				if(status == 0x0)
				{
					break;
				}
				msleep(10);
			}		
		}
		else
                    return -EINVAL;
		break;	
	
	case V4L2_CID_TEST_PATTERN: {
		printk("ioctl_queryctrl: V4L2_CID_TEST_PATTERN \n");
		switch (vc->value) {
			case TEST_PATTERN_COLOR_BAR:
				ov5640_write_reg(0x4741, 0x00);
				ov5640_write_reg(0x503d, 0x80);
			break;
			case TEST_PATTERN_COLOR_SQUARE:
				ov5640_write_reg(0x4741, 0x00);
				ov5640_write_reg(0x503d, 0x82);
			break;
			case TEST_PATTERN_BLACK_WHITE_SQUARE:
				ov5640_write_reg(0x4741, 0x00);
				ov5640_write_reg(0x503d, 0x92);
			break;
			case TEST_PATTERN_DISABLE:
			default:
				ov5640_write_reg(0x503d, 0x00);
				ov5640_write_reg(0x4741, 0x00);
			break;
		}
		ov5640_data.pattern = vc->value;
		return 0;
	}break;
	
	case V4L2_CID_COLORFX: {
		switch (vc->value) {
			case V4L2_COLORFX_NONE:
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x00, 0xF8);
			break;
			case V4L2_COLORFX_BW:
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x18, 0x5A);
				ov5640_write_reg(0x5583, 0x80);
				ov5640_write_reg(0x5584, 0x80);
			break;
			case V4L2_COLORFX_SEPIA:
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x18, 0x5A);
				ov5640_write_reg(0x5583, 0x40);
				ov5640_write_reg(0x5584, 0xa0);
			break;
			case V4L2_COLORFX_NEGATIVE:
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x40, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x40, 0x40);
			break;
			case V4L2_COLORFX_SKY_BLUE:
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x18, 0x5A);
				ov5640_write_reg(0x5583, 0xa0);
				ov5640_write_reg(0x5584, 0x40);
			break;
			case V4L2_COLORFX_GRASS_GREEN: 
				WRITE_REG_WITH_OVT_MAST(0x5588, 0x00, 0x40);
				WRITE_REG_WITH_OVT_MAST(0x5001, 0x80, 0x80);
				WRITE_REG_WITH_OVT_MAST(0x5580, 0x18, 0x5A);
				ov5640_write_reg(0x5583, 0x60);
				ov5640_write_reg(0x5584, 0x60);
			break;
			default:
                               	return -EINVAL;
                        break;
		}
		ov5640_data.effects = vc->value;
		return 0;
	}break;

	
	case V4L2_CID_DO_WHITE_BALANCE:
		break;
	case V4L2_CID_RED_BALANCE:
		break;
	case V4L2_CID_BLUE_BALANCE:
		break;
	case V4L2_CID_GAMMA:
		break;

	case V4L2_CID_EXPOSURE:
		OV5640_set_shutter(vc->value);
		break;
	case V4L2_CID_AUTOGAIN:
		OV5640_turn_on_AE_AG(vc->value);
		ov5640_data.ae_mode = vc->value;
		break;
	case V4L2_CID_GAIN:
		OV5640_set_gain16(vc->value);
		break;

	default:
		retval = -EPERM;
		break;
	}

	return retval;
}

/*!
 * ioctl_enum_framesizes - V4L2 sensor interface handler for
 *			   VIDIOC_ENUM_FRAMESIZES ioctl
 * @s: pointer to standard V4L2 device structure
 * @fsize: standard V4L2 VIDIOC_ENUM_FRAMESIZES ioctl structure
 *
 * Return 0 if successful, otherwise -EINVAL.
 */
static int ioctl_enum_framesizes(struct v4l2_int_device *s,
				 struct v4l2_frmsizeenum *fsize)
{
	if (fsize->index > ov5640_mode_MAX)
		return -EINVAL;

	fsize->pixel_format = ov5640_data.pix.pixelformat;
	fsize->discrete.width =
			max(ov5640_mode_info_data[0][fsize->index].width,
			    ov5640_mode_info_data[1][fsize->index].width);
	fsize->discrete.height =
			max(ov5640_mode_info_data[0][fsize->index].height,
			    ov5640_mode_info_data[1][fsize->index].height);
	return 0;
}

/*!
 * ioctl_enum_frameintervals - V4L2 sensor interface handler for
 *			       VIDIOC_ENUM_FRAMEINTERVALS ioctl
 * @s: pointer to standard V4L2 device structure
 * @fival: standard V4L2 VIDIOC_ENUM_FRAMEINTERVALS ioctl structure
 *
 * Return 0 if successful, otherwise -EINVAL.
 */
static int ioctl_enum_frameintervals(struct v4l2_int_device *s,
					 struct v4l2_frmivalenum *fival)
{
	int i, j, count = 0;

	fival->type = V4L2_FRMIVAL_TYPE_DISCRETE;
	fival->discrete.numerator = 1;

	for (i = 0; i < ARRAY_SIZE(ov5640_mode_info_data); i++)
		for (j = 0; j < (ov5640_mode_MAX + 1); j++)
			if (fival->pixel_format == ov5640_data.pix.pixelformat
			 && fival->width == ov5640_mode_info_data[i][j].width
			 && fival->height == ov5640_mode_info_data[i][j].height
			 && ov5640_mode_info_data[i][j].init_data_ptr != NULL
			 && fival->index == count++) {
				fival->discrete.denominator =
						ov5640_framerates[i];
				return 0;
			}

	return -EINVAL;
}

